      FUNCTION GET_ADDR(VAR)
C     ==--------------------------------------------------------------==
      IMPLICIT NONE
      REAL*8  VAR
#ifdef POINTER8
      INTEGER*8 GET_ADDR,LOC
#else
      INTEGER   GET_ADDR,LOC
#endif
C     ==--------------------------------------------------------------==
#ifdef __SR2201
      GET_ADDR=%LOC(VAR)
#else
      GET_ADDR=LOC(VAR)
#endif
C     ==--------------------------------------------------------------==
      RETURN
      END
